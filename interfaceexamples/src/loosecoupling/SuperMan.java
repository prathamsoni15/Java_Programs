package loosecoupling;

public class SuperMan implements GameLauncher{

	public void left() {
		
		System.out.println("S Go Back");
		
	}
	
	public void right() {
		
		System.out.println("S Go Forward");
		
	}
	
	public void up() {
		
		System.out.println("S Jump");
		
	}
	
	public void down() {
		
		System.out.println("S Go Inside tunnels");
		
	}
	
}
